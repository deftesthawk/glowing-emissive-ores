function generateSupportedModList(selectedPlatform) {
  const ul = document.querySelector("#supported .spoiler ul");
  if (!ul) return;

  ul.innerHTML = "";

  // Static 'Vanilla' entry
  const vanillaLi = document.createElement("li");
  vanillaLi.textContent = "Vanilla";
  ul.appendChild(vanillaLi);

  for (const modName in SupportedMods) {
    const mod = SupportedMods[modName];
    const link = mod[selectedPlatform] || mod.CurseForge || mod.Modrinth;

    const li = document.createElement("li");

    if (link) {
      const a = document.createElement("a");
      a.href = link;
      a.target = "_blank";
      a.textContent = modName;
      a.setAttribute("data-mod", modName);
      a.style.color = "var(--link)";
      a.style.textDecoration = "none";
      li.appendChild(a);
    } else {
      li.textContent = `${modName} (No link available)`;
    }

    ul.appendChild(li);
  }
}

// Get URL parameters
const params = new URLSearchParams(window.location.search);
let platform = params.get('platform');

// Fallback logic if no platform is specified or if the platform is invalid
if (!platform || (platform !== 'modrinth' && platform !== 'curseforge')) {
  // Show the platform selection modal
  showPlatformSelectionModal();
} else {
  const preferredPlatform = (platform === 'modrinth') ? 'Modrinth' : 'CurseForge';
  updateLinks(preferredPlatform);
  generateSupportedModList(preferredPlatform);
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll('a[data-mod]').forEach(link => {
    const modName = link.getAttribute('data-mod');
    const modInfo = ModLinks[modName];

    if (!modInfo) return;

    const href = modInfo[platform === 'modrinth' ? 'Modrinth' : 'CurseForge'] || modInfo.CurseForge || modInfo.Modrinth;

    if (href) {
      link.href = href;
    } else {
      link.style.pointerEvents = 'none';
      link.style.opacity = '0.5';
      link.textContent += " (Unavailable)";
    }
  });
});

// Platform dependant links
const platformSelector = document.getElementById("platformSelector");
function updateLinks(selectedPlatform) {
  const links = document.querySelectorAll("a[data-mod]");

  links.forEach(link => {
    const modName = link.getAttribute("data-mod");
    const mod = ModLinks[modName];

    if (mod) {
      link.href = mod[selectedPlatform] || mod.CurseForge || "#";
    }
  });
}

// Event listener for the platform selector
platformSelector.addEventListener("change", () => {
  const selectedPlatform = platformSelector.value.toLowerCase();
  updateLinks(selectedPlatform);
  generateSupportedModList(selectedPlatform);
  window.location.search = `?platform=${selectedPlatform}`;
});


